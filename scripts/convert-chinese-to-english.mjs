function getFileListFromListFile() {
  const listPath = path.join(__dirname, 'chinese-to-english-files-list.txt')
  const raw = fs.readFileSync(listPath, 'utf8')
  const lines = raw.split('\n').map(l => l.trim()).filter(l => l && !l.startsWith('#'))
  return lines
    .filter(rel => !rel.endsWith('.zh.txt') && !rel.includes('convert-chinese-to-english.mjs'))
    .map(rel => path.join(root, rel))
    .filter(p => {
      try {
        return fs.statSync(p).isFile()
      } catch {
        return false
      }
    })
}

function walk(dir, exts, list = []) {
  const entries = fs.readdirSync(dir, { withFileTypes: true })
  for (const e of entries) {
    const full = path.join(dir, e.name)
    if (e.isDirectory()) {
      if (e.name === 'node_modules' || e.name === '.git') continue
      walk(full, exts, list)
    } else if (exts.some(ext => e.name.endsWith(ext))) {
      list.push(full)
    }
  }
  return list
}

const listFile = path.join(__dirname, 'chinese-to-english-files-list.txt')
const useList = fs.existsSync(listFile)
const files = useList
  ? getFileListFromListFile()
  : walk(root, ['.ts', '.tsx', '.js', '.jsx', '.mjs']).filter(
      f => !f.includes('convert-chinese-to-english.mjs')
    )

let total = 0
for (const file of files) {
  let content = fs.readFileSync(file, 'utf8')
  let changed = false
  for (const [from, to] of REPLACEMENTS) {
    if (content.includes(from)) {
      content = content.split(from).join(to)
      changed = true
    }
  }
  if (changed) {
    fs.writeFileSync(file, content)
    total++
  }
}
console.log(`Updated ${total} files`)
